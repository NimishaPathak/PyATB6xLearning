# Comparison operators
x, y = 10, 20
print(x == y); # false -> equal to operator
print(x != y); # true -> not equal to operator
print(x > y); # false -> greater than operator
print(x < y); # true -> less than operator

print("---")

print(2 == 2) # true
print(2 == 3) # false

print("---")

x = 10
y = 20

result = (x != y)
print(result); # true