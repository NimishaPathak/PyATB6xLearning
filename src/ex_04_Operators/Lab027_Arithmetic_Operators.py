# 1. Arithmetic operators
a, b = 10, 3;
print(a+b); # 13 -> addition
print(a - b); #7 -> substraction
print(a * b);# 30 -> multiplication
print(a / b); # 3.333 -> division (returns float)
print(a // b); # 3 -> Floor division (integer division) / Quotient
print(a % b); # 1 -> moduls (reminder)
print(a ** b); # 1000 exponentiation (power)

